//初始化IIC
void IIC_Init(void)
{					     
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(	RCC_APB2Periph_GPIOE, ENABLE );	
	   
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2|GPIO_Pin_3;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP ;   //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOE, &GPIO_InitStructure);
	GPIO_SetBits(GPIOE,GPIO_Pin_2|GPIO_Pin_3); 	//PB10,PB11 输出高
}
//产生IIC起始信号
void IIC_Start(void)
{
	SDA_OUT();     //sda线输出
	GPIO_SetBits(GPIOE, GPIO_Pin_3) ;	  	  
	GPIO_SetBits(GPIOE, GPIO_Pin_2) ;
	delay_us(4);
 	GPIO_ResetBits(GPIOE, GPIO_Pin_3) ;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;//钳住I2C总线，准备发送或接收数据 
}	  
//产生IIC停止信号
void IIC_Stop(void)
{
	SDA_OUT();//sda线输出
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;
	GPIO_ResetBits(GPIOE, GPIO_Pin_3) ;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	GPIO_SetBits(GPIOE, GPIO_Pin_2) ; 
        	delay_us(4);	
	GPIO_SetBits(GPIOE, GPIO_Pin_3) ;//发送I2C总线结束信号
						   	
}
//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
unsigned char IIC_Wait_Ack(void)
{
	unsigned char ucErrTime=0;
	SDA_IN();      //SDA设置为输入  
	GPIO_SetBits(GPIOE, GPIO_Pin_3) ;delay_us(1);	   
	GPIO_SetBits(GPIOE, GPIO_Pin_2) ;delay_us(1);	 
	while(READ_SDA)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			IIC_Stop();
			return 1;
		}
	}
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;//时钟输出0 	   
	return 0;  
} 
//产生ACK应答
void IIC_Ack(void)
{
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;
	SDA_OUT();
	GPIO_ResetBits(GPIOE, GPIO_Pin_3) ;
	delay_us(2);
	GPIO_SetBits(GPIOE, GPIO_Pin_2) ;
	delay_us(2);
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;
}
//不产生ACK应答		    
void IIC_NAck(void)
{
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;
	SDA_OUT();
	GPIO_SetBits(GPIOE, GPIO_Pin_3) ;
	delay_us(2);
	GPIO_SetBits(GPIOE, GPIO_Pin_2) ;
	delay_us(2);
	GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;
}					 				     
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC_Send_Byte(unsigned char txd)
{                        
    unsigned char t;   
	SDA_OUT(); 	    
    GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;//拉低时钟开始数据传输
    for(t=0;t<8;t++)
    {              
        //IIC_SDA=(txd&0x80)>>7;
		if((txd&0x80)>>7)
			GPIO_SetBits(GPIOE, GPIO_Pin_3) ;
		else
			GPIO_ResetBits(GPIOE, GPIO_Pin_3) ;
		txd<<=1; 	  
		delay_us(2);   //对TEA5767这三个延时都是必须的
		GPIO_SetBits(GPIOE, GPIO_Pin_2) ;
		delay_us(2); 
		GPIO_ResetBits(GPIOE, GPIO_Pin_2) ;	
		delay_us(2);
    }	 
} 	    
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
unsigned char IIC_Read_Byte(unsigned char ack)
{
	unsigned char i,receive=0;
	SDA_IN();//SDA设置为输入
    for(i=0;i<8;i++ )
	{
        GPIO_ResetBits(GPIOE, GPIO_Pin_2) ; 
        delay_us(2);
		GPIO_SetBits(GPIOE, GPIO_Pin_2) ;
        receive<<=1;
        if(READ_SDA)receive++;   
		delay_us(1); 
    }					 
    if (!ack)
        IIC_NAck();//发送nACK
    else
        IIC_Ack(); //发送ACK   
    return receive;
}



























